let month = "February";


switch (month) {
    case "Spring":
        console.log("Flower Power");

        break;
        case "Summer":
        console.log("C'est l'été!");

        break;
        case "Autumn":
        console.log("Orange");

        break;
        case "January", "February":
        console.log("Winter");
        
        break;
        default:
        console.log("no season");
}